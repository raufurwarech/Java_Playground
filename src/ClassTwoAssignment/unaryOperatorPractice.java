package ClassTwoAssignment;

public class unaryOperatorPractice {

    public static void main(String[] args) {
        int number = 70;

        System.out.println(number++);
        System.out.println(number);
        System.out.println(--number);
        System.out.println(number);
        System.out.println(number--);
        System.out.println(++number);
        System.out.println(number);

    }
}
