package ClassTwoAssignment;

public class UnaryOperator {
    public static void main(String[] args) {
        int mark=65;

        mark = mark+1; //++
        mark = mark-1; //--

        System.out.println(mark++);
        System.out.println(mark);
        System.out.println(--mark);
        System.out.println(mark);
        System.out.println(mark--);
        System.out.println(++mark);
        System.out.println(mark);

    }
}


